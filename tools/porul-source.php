10

மழையினால் இவ்வுலகம் செழிப்புற்று வாழ்ந்து வருகின்றது. எனவே உலக உயிர்களுக்கு அம்மழை அமிழ்து போன்றது.

11

நல்லுணவுகளை உண்டாக்கி உண்பவர்களித்து, தானும் குடிநீராய் பயன்படுவது மழை.

12

மழை தவறாது பெய்யாமல் பொய்க்குமாயின், கடல் சூழ்ந்த இவ்வுலகில் பசி நிலைத்து உயிர்கள் வருந்தும்.

13

மழையின் வளம் குறையுமானால் உழவர்கள் உழுவதற்கு ஏர் பிடிக்க மாட்டார்கள்.

14

பெய்யாமல் மக்கள் வாழ்வைக் கெடுப்பதும் மழை. பெய்து மக்களை வளமுடன் வாழவைப்பதும் மழை.

15

வானிலிருந்து துளி மழை பெய்யாது போனால், பசுமை யான புல்லின் நுனியையும் காணவியலாது.

16

மேகம் கடல் நீரைக்குடித்து பெய்யாமல் போனால், கடலிடத்துள்ள செல்வங்களும் குறையும்.

17

வானம் பொய்த்து, மழை பெய்யவில்லையானால், தேவர்களுக்கும்; பூசையும், திருவிழாக்களுக்கும் நடவாது,

18

மழை பெய்யாதாயின் உலகில் பிறர்க்கு கொடுக்கும் தானமும் தம் பொருட்டுச் செய்யும் தவமும் நடவாது.

19

நீரில்லாமல் உலக வாழ்க்கை அமையாது. அத்தகைய மழையில்லையேல் எவ்வகையில் உயர்ந்தவரிடத்தும் ஒழுக்கம் நிலைக்காது.

20

நல்லொழுக்க நெறியில் நின்று முற்றம் துறந்த முனிவரது பெருமையையே நூல்கள்சிறந்த பெருமையாக எடுத்துக் கூறும்.

21

துறவிகளின் பெருமையை இவ்வளவினது என்று கூற முற்படுதல், உலகில் இறந்தவர்களை எண்ணி அளவிட முற்படுவது போன்றதாகும்.

22

உலகியல், வீட்டுநெறியின் தன்மைகளை ஆராய்ந்து துறவு வாழ்க்கையை மேற்கொண்டோரின் பெருமையை போன்று சிறந்தது வேறில்லை.

23

அறிவெனும் முள் கொண்டு, ஐம்புலன்கள் எனும் யானையை அடக்கி ஆள்பவனே முக்தி அடைவதற்கு ஏற்றவன்.

24

ஐம்பொறிகளையும் அடக்கியவனது வல்லமைக்கு விண் உலக தேவர்களின் தலைவனாகிய இந்திரனே சான்றாவான்.

25

